# Prg 1: Printing numbers up to 10 using while loop.

i = 1
while i <= 10:
    print(i)
    i = i + 1

# Prg 2: Printing * up to 10 times using while loop.
    
i = 1
while i <= 10:
    print(i * "*")
    i = i + 1

# Prg 3: Printing reverse * up to 10 times using while loop.
    
i = 10
while i >= 0:
    print(i * "*")
    i = i - 1

# Prg 4: Printing reverse numbers up to 10 times using while loop.

i = 10
while i >= 0:
    print(i)
    i = i - 1